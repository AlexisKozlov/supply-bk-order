#!/usr/bin/env bash
# Атомарная сборка фронта с переносом старых чанков.
#
# Зачем атомарность: сборка идёт ~3-4 минуты. Авто-хук, который её
# запускает, может оборваться раньше (новое действие пользователя или
# таймаут). Раньше build.sh сразу отодвигал рабочую dist в сторону и
# собирал новую на её месте — если процесс убивали посреди сборки,
# прод оставался с недостроенной dist без sw.js, а проверка и откат
# в конце скрипта не успевали выполниться (убит сам скрипт).
#
# Теперь сборка идёт в отдельную папку dist.staging. Живой dist всё
# это время работает на проде. Подмена происходит одной операцией
# переименования в самом конце — только если сборка прошла целиком и
# sw.js на месте. Оборванная сборка физически не может задеть прод:
# в худшем случае останется огрызок dist.staging, который следующий
# запуск просто удалит.
#
# Перенос старых чанков: ленивые модальные/тяжёлые чанки (см.
# globIgnores в vite.config.js) не попадают в precache Service
# Worker'а. Если пользователь держит открытой вкладку через деплой и
# нажимает «Позже» в баннере обновления, его старый бандл при попытке
# открыть модалку обращается за чанком по старому хешу. Чтобы он не
# исчез сразу, недостающие чанки подкладываются из предыдущей сборки
# (живого dist) ещё на один цикл.

set -euo pipefail
cd "$(dirname "$0")/.."

# npm run кладёт node_modules/.bin в PATH, но при ручном запуске — нет.
export PATH="$PWD/node_modules/.bin:$PATH"

# Блокировка от параллельных запусков (ручной build + Stop-hook
# одновременно). -n: если другой билд уже идёт — молча пропускаем,
# а не висим в очереди.
exec 200>/tmp/bk-calc-build.lock
flock -n -x 200 || { echo "[build.sh] another build in progress — skipping"; exit 0; }

STAGE="dist.staging"

# На сервере 2 ГБ RAM, и node 20 на маленькой машине ставит heap-лимит
# ~1 ГБ. Vite + PWA-плагин в конце сборки иногда упирается в этот
# потолок и падает с OOM. Поднимаем потолок до 2 ГБ — недостающее
# уйдёт в swap, но сборка не сорвётся.
export NODE_OPTIONS="${NODE_OPTIONS:-} --max-old-space-size=2048"

# Чистим огрызок прошлой оборванной сборки, если он остался.
# Живой dist при этом не трогаем.
rm -rf "$STAGE"

# Если скрипт упадёт или его убьют до подмены — staging убирается за
# собой, мусор не копится. После успешной подмены trap снимается.
trap 'rm -rf "$STAGE"' EXIT

# Сборка фронта в отдельную папку. Прод всё это время живой.
vite build --outDir "$STAGE"

# Проверка целостности: sw.js пишется плагином PWA в самом конце
# сборки. Нет sw.js — значит сборка оборвана. Выходим с ошибкой,
# живой dist не тронут.
if [ ! -f "$STAGE/sw.js" ]; then
  echo "[build.sh] ОШИБКА: $STAGE/sw.js не появился — сборка повреждена. Прод не тронут."
  exit 1
fi

# PHP API в сборку.
mkdir -p "$STAGE/api"
cp -r api/*.php api/includes api/migrations "$STAGE/api/"

# Подкладываем в новую сборку чанки из текущего живого dist, которые
# в ней не появились. `cp -n` (no-clobber) не перетирает свежие
# файлы — остаются только старые хеши, чтобы пользователи на N-1
# версии могли дотянуть с лениво подгружаемыми чанками.
if [ -d dist/assets ]; then
    before=$(find "$STAGE/assets" -maxdepth 1 -type f | wc -l)
    # -p сохраняет реальные mtime переносимых чанков, чтобы старые потом
    # отсеялись по возрасту (см. чистку после подмены). Без -p все чанки
    # получали бы свежую дату и копились бы бесконечно (была причина
    # переполнения диска и падения MariaDB).
    cp -pnR dist/assets/. "$STAGE/assets/" 2>/dev/null || true
    after=$(find "$STAGE/assets" -maxdepth 1 -type f | wc -l)
    echo "[build.sh] preserved $((after - before)) old chunks from current dist"
else
    echo "[build.sh] no current dist/assets — skipping preservation (первая сборка)"
fi

# Атомарная подмена. Окно недоступности — две операции переименования
# на одной файловой системе, доли миллисекунды (раньше прод лежал в
# 404/503 на все 3-4 минуты сборки из-за rm -rf dist).
# Прошлую dist оставляем как dist.prev — на случай ручного отката.
rm -rf dist.prev
[ -d dist ] && mv dist dist.prev
mv "$STAGE" dist

# Ограничиваем накопление старых чанков: удаляем ассеты старше 14 дней.
# Свежая сборка их не использует; пользователи на старых версиях за 2 недели
# уже обновляются. Не даёт dist/assets расти бесконечно (иначе переполняется
# диск — это уже роняло MariaDB). Текущие чанки имеют свежую дату и остаются.
find dist/assets -maxdepth 1 -type f -mtime +14 -delete 2>/dev/null || true

# Сборка успешно подменена — staging больше нет, чистить нечего.
trap - EXIT
echo "[build.sh] сборка завершена, dist подменён атомарно"
