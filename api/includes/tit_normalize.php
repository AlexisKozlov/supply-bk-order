<?php
/**
 * Нормализация номера машины и телефона водителя для модуля «Заявка на пропуск».
 *
 * Используется и при парсинге email-ответа поставщика, и при ручном вводе в
 * портале — одна и та же функция гарантирует одинаковый формат и одинаковую
 * валидацию по обе стороны.
 *
 * Машина: «АМ2324-5» / «am 23 24 5» → «AM23245»
 * Телефон: «+375 (29) 537-43-11» / «80 29 537 43 11» → «375295374311»
 */

/**
 * Карта кириллических букв, визуально совпадающих с латинскими, в латиницу.
 * Берётся ровно тот же набор, что используют у нас номерные знаки.
 */
function titCyrillicToLatinMap(): array
{
    return [
        // Заглавные
        'А' => 'A', 'В' => 'B', 'Е' => 'E', 'К' => 'K', 'М' => 'M',
        'Н' => 'H', 'О' => 'O', 'Р' => 'P', 'С' => 'C', 'Т' => 'T',
        'У' => 'Y', 'Х' => 'X',
        // Строчные — нормализуем к заглавным сразу
        'а' => 'A', 'в' => 'B', 'е' => 'E', 'к' => 'K', 'м' => 'M',
        'н' => 'H', 'о' => 'O', 'р' => 'P', 'с' => 'C', 'т' => 'T',
        'у' => 'Y', 'х' => 'X',
    ];
}

/**
 * Нормализует номер машины.
 *
 * @param string|null $input исходное значение от поставщика или из ручного ввода
 * @return array{plate: string, raw: string, valid: bool}
 *   plate — нормализованный номер (только [A-Z0-9])
 *   raw   — исходная строка (обрезанная trim)
 *   valid — true, если получили строку длиной от 5 до 10 символов
 */
function titNormalizePlate(?string $input): array
{
    $raw = trim((string)$input);
    if ($raw === '') return ['plate' => '', 'raw' => '', 'valid' => false];

    // Заменяем визуально совпадающие кириллические буквы на латинские
    $s = strtr($raw, titCyrillicToLatinMap());

    // Латиница → верхний регистр (mb_strtoupper для безопасности с не-ASCII)
    $s = mb_strtoupper($s, 'UTF-8');

    // Оставляем только латинские буквы и цифры
    $plate = preg_replace('/[^A-Z0-9]/u', '', $s) ?? '';

    $len = strlen($plate);
    $valid = $len >= 5 && $len <= 10;

    return ['plate' => $plate, 'raw' => $raw, 'valid' => $valid];
}

/**
 * Нормализует телефон водителя к формату «375XXXXXXXXX» (12 цифр, без «+»).
 *
 * Правила:
 *   - убираем всё, кроме цифр;
 *   - если получилось 11 цифр с начала «8» (формат 80XXXXXXXXX) — меняем 8 на 375;
 *   - если уже начинается с 375 и длина 12 — оставляем как есть;
 *   - всё прочее — невалидно.
 *
 * @param string|null $input
 * @return array{phone: string, raw: string, valid: bool}
 */
function titNormalizePhone(?string $input): array
{
    $raw = trim((string)$input);
    if ($raw === '') return ['phone' => '', 'raw' => '', 'valid' => false];

    // Оставляем только цифры
    $digits = preg_replace('/\D+/', '', $raw) ?? '';

    // 80XXXXXXXXX (11 цифр, белорусский национальный) → 375XXXXXXXXX
    // «8» — национальный префикс выхода, «0» — обозначение кода города/оператора.
    // В международном формате «80» заменяется на «375» целиком.
    if (strlen($digits) === 11 && str_starts_with($digits, '80')) {
        $digits = '375' . substr($digits, 2);
    }

    // Допустимый формат: ровно 12 цифр, начинается с 375
    $valid = strlen($digits) === 12 && str_starts_with($digits, '375');

    return ['phone' => $valid ? $digits : '', 'raw' => $raw, 'valid' => $valid];
}

/**
 * Строгая проверка: похоже ли на белорусский регистрационный знак.
 *
 * Нужна ТОЛЬКО для автоматического разбора писем и накладных, где кандидатов
 * ищет регекс и в них попадает мусор. Реальный пример: из служебной метки
 * картинки в подписи «[cid:image001.png@01DD0A3D.2A284D30]» распознавался
 * «номер машины» 2A284D30.
 *
 * Ручной ввод в портале этой проверкой НЕ ограничен — там закупщик может
 * ввести и иностранный номер.
 *
 * Допустимые формы (после нормализации, только A-Z0-9):
 *   AB12345  — 2 буквы + 4-5 цифр (старый формат вместе с кодом региона)
 *   1234AB7  — 4 цифры + 2 буквы + опциональный код региона (новый формат)
 *
 * Однобуквенные («D5067») намеренно не принимаем: в письмах такое не
 * встречается, зато под этот шаблон попадали куски адресов и номеров
 * документов из подписи.
 */
function titLooksLikePlate(string $plate): bool
{
    if ($plate === '') return false;
    if (preg_match('/^[A-Z]{2}\d{4,5}$/', $plate)) return true;
    // Новый формат «1234 AB-7» — код региона обязателен. Без него под шаблон
    // попадает год из накладной с обрывком букв рядом: «09.2022» + «ет» давали
    // «номер» 2022ET. Все настоящие номера в базе код региона имеют.
    if (preg_match('/^\d{4}[A-Z]{2}\d$/', $plate)) return true;
    return false;
}

/**
 * Краткая проверка валидности заполненной пары машина+телефон.
 * Возвращает массив проблем (пустой = всё ок).
 */
function titValidatePair(?string $plate, ?string $phone): array
{
    $issues = [];
    $p = titNormalizePlate($plate);
    $t = titNormalizePhone($phone);
    if (!$p['valid']) $issues[] = 'Номер машины: 5–10 символов, только буквы и цифры';
    if (!$t['valid']) $issues[] = 'Телефон водителя: 12 цифр, формат 375XXXXXXXXX';
    return $issues;
}
