<?php
/**
 * Парсер ответа поставщика для модуля «Заявка на пропуск».
 *
 * На вход — тело входящего email-ответа (текст). На выходе — список найденных
 * пар «номер машины + телефон водителя» в нормализованном виде. Логика
 * полагается на якорные слова («машина», «номер», «авто», «водитель»,
 * «телефон»), отрезает подпись и цитированный текст, чтобы не цеплять
 * телефон из «С уважением, Иван +375…».
 */

require_once __DIR__ . '/tit_normalize.php';

/**
 * Чистит входящий текст: убирает quote-блок (цитата нашего письма),
 * подпись, нормализует переносы.
 */
function titCleanReplyBody(string $text): string
{
    // Унифицируем переносы строк
    $text = preg_replace("/\r\n|\r/", "\n", $text) ?? $text;

    // Вычищаем служебный мусор письма ДО всякого поиска. Он не несёт данных,
    // зато отлично притворяется номером машины: из метки картинки в подписи
    // «[cid:image001.png@01DD0A3D.2A284D30]» парсер вытаскивал «номер» 2A284D30.
    $noise = [
        '/\[cid:[^\]]*\]/i',                 // [cid:image001.png@01DD0A3D.2A284D30]
        '/\bcid:[^\s>\]]+/i',                // cid:… без скобок
        '/<mailto:[^>]*>/i',
        '/<https?:\/\/[^>]*>/i',
        '/\bhttps?:\/\/\S+/i',
        '/\b[\w\.\-]+\.(?:png|jpe?g|gif|bmp|webp)\b/i',   // image001.png
        '/\[[^\]]*AI-generated content[^\]]*\]/i',        // подпись-заглушка Outlook
    ];
    foreach ($noise as $rx) {
        $text = preg_replace($rx, ' ', $text) ?? $text;
    }

    // Отрезаем цитированную часть: всё, что после стандартных маркеров
    // или начиная с первой строки, начинающейся с «>».
    $markers = [
        '/(^|\n)\s*>+\s.*/s',                         // строки, начинающиеся с «>»
        '/(^|\n)\s*On\s+.+?wrote:.*/si',              // English «On … wrote:»
        '/(^|\n)\s*В\s+.+?\sписал[аи]?:.*/su',        // «В … писал:»
        '/(^|\n)\s*-----\s*Original Message\s*-----.*/si',
        '/(^|\n)\s*От:.*\n\s*Кому:.*/su',              // Outlook-like header
        '/(^|\n)\s*From:.*\n\s*Sent:.*/si',
        '/(^|\n)\s*Кому:\s*\S+@.*/su',                 // цитата Яндекс-почты
        '/(^|\n)\s*\d{2}\.\d{2}\.\d{4},\s*\d{1,2}:\d{2},\s*".*/su', // «02.07.2026, 14:48, "Отдел закупок" <…>:»
    ];
    foreach ($markers as $rx) {
        if (preg_match($rx, $text, $m, PREG_OFFSET_CAPTURE)) {
            $text = substr($text, 0, $m[0][1]);
        }
    }

    // Отрезаем подпись: «С уважением», «С почтением», «Best regards»,
    // длинная строка тире/подчёркиваний и подобное.
    $signatureMarkers = [
        '/(^|\n)\s*С\s+уважением[\s,].*/su',
        '/(^|\n)\s*С\s+почтением[\s,].*/su',
        '/(^|\n)\s*Best\s+regards.*/si',
        '/(^|\n)\s*Kind\s+regards.*/si',
        '/(^|\n)\s*BR[,\s].*/si',
        '/(^|\n)\s*--\s*\n.*/s',                     // классический «-- » как граница подписи
        '/(^|\n)\s*[—_-]{3,}\s*\n.*/s',              // разделитель из тире/подчёркиваний
    ];
    foreach ($signatureMarkers as $rx) {
        if (preg_match($rx, $text, $m, PREG_OFFSET_CAPTURE)) {
            $text = substr($text, 0, $m[0][1]);
        }
    }

    // Отрезаем подпись без явного маркера — у Outlook её часто нет вовсе,
    // просто идут имя, должность, компания, телефон и сайт. Такой блок давал
    // телефон менеджера вместо телефона водителя.
    $text = titCutContactBlock($text);

    return trim($text);
}

/**
 * Обрезает текст с первой строки, которая выглядит как начало контактного
 * блока (подписи): адрес почты, сайт, должность, «T: +375…», реквизиты.
 *
 * Телефон водителя пишут в теле ответа («Данные по машине: АВ1234-5,
 * +375291234567»), а не в строке с сайтом или должностью — поэтому такой
 * блок можно отсекать целиком, не боясь потерять данные.
 */
function titCutContactBlock(string $text): string
{
    $lines = explode("\n", $text);
    foreach ($lines as $i => $line) {
        if (titLineLooksLikeSignature($line)) {
            return implode("\n", array_slice($lines, 0, $i));
        }
    }
    return $text;
}

/**
 * Строка из подписи/контактного блока, а не данные?
 *
 * Строку с якорем («телефон водителя …», «машина …») подписью не считаем,
 * даже если она начинается с «Тел.» — там как раз данные.
 */
function titLineLooksLikeSignature(string $line): bool
{
    if (preg_match('/\b(машин|водител|авто|тягач|транспорт|ттн|прицеп)/iu', $line)) return false;

    // Кириллическую строку «Тел. +375…» подписью НЕ считаем: телефон водителя
    // поставщики пишут именно так («Данные водителя: … \n Тел. +375297796779»).
    // А вот латинские «T:» / «Tel:» — это outlook-подпись, там телефон менеджера.
    $signals = [
        '/[\w\.\-]+@[\w\.\-]+\.\w{2,}/u',                        // email в строке
        '/\bwww\.\w/iu',                                         // сайт
        '/^\s*(?:T|M|Tel|Mob|Fax)\s*[:.]\s*\+?\d/u',             // «T: +375(29)648 83 31»
        '/\b(?:manager|managing|director|specialist|coordinator|supervisor)\b/iu',
        '/\b(?:менеджер|директор|специалист|координатор|руководител|начальник|бухгалтер)\w*/iu',
        '/\b(?:спецыяліст|намеснік|дырэктар)\w*/iu',             // подписи на белорусском
        '/\b(?:УНП|р\/с|IBAN|BIC)\b/iu',                         // реквизиты
    ];
    foreach ($signals as $rx) {
        if (preg_match($rx, $line)) return true;
    }
    return false;
}

/**
 * Препроцессор: склеивает белорусские номера, разорванные пробелами/дефисами,
 * в один плотный фрагмент. После этого основной регекс ловит их как обычно.
 *
 *   «АС 6668-5»    → «АС6668-5»     (буквы + пробел + цифры с регионом)
 *   «BB-10-18-5»   → «BB10185»      (многосегментный — три группы цифр)
 *   «1234 AB-7»    → «1234AB-7»     (новый формат: цифры впереди)
 *
 * Word-boundary берётся по Unicode-классам (\p{L}\p{N}), чтобы не задеть
 * «Сумма 1234 руб» — после цифр идёт «руб», граница не сработает.
 */
function titNormalizePlateSpacing(string $text): string
{
    $A = 'A-Za-zАВЕКМНОРСТУХавекмнорстух';
    // 1. Многосегментный letter-first: «BB-10-18-5» → «BB10185»
    $text = preg_replace_callback(
        '/(?<![\p{L}\p{N}])([' . $A . ']{1,2})[\-\s]+(\d{1,4})[\-\s]+(\d{1,4})[\-\s]+(\d{1,2})(?![\p{L}\p{N}])/u',
        fn($m) => $m[1] . $m[2] . $m[3] . $m[4],
        $text
    ) ?? $text;
    // 2. Letter-first 2-group: «АС 6668-5» → «АС6668-5», «AB-12345» → «AB12345»
    $text = preg_replace_callback(
        '/(?<![\p{L}\p{N}])([' . $A . ']{1,2})[\-\s]+(\d{4,6})((?:[\-\s]\d{1,2})?)(?![\p{L}\p{N}])/u',
        fn($m) => $m[1] . $m[2] . $m[3],
        $text
    ) ?? $text;
    // 3. Digits-first (новый формат): «1234 AB-7» → «1234AB-7»
    $text = preg_replace_callback(
        '/(?<![\p{L}\p{N}])(\d{4,6})[\-\s]+([' . $A . ']{1,2})((?:[\-\s]\d{1,2})?)(?![\p{L}\p{N}])/u',
        fn($m) => $m[1] . $m[2] . $m[3],
        $text
    ) ?? $text;
    // 4. Тот же новый формат, но с буквами в конце — так пишут поставщики:
    //    «Газель 1197-5 BA» → «1197BA5». Номер тот же, что «1197 ВА-5».
    $text = preg_replace_callback(
        '/(?<![\p{L}\p{N}])(\d{4})[\-\s](\d{1,2})[\-\s]([' . $A . ']{2})(?![\p{L}\p{N}])/u',
        fn($m) => $m[1] . $m[3] . $m[2],
        $text
    ) ?? $text;
    return $text;
}

/**
 * Найти все кандидаты на номер машины в тексте. Возвращает массив
 * объектов {plate, raw, pos} — нормализованный номер, исходный фрагмент
 * и позиция в тексте (для связывания с телефоном по близости).
 */
function titFindPlateCandidates(string $text): array
{
    // Препроцессор склейки делается в titParseReplyBody на исходном тексте,
    // чтобы pos оставались согласованными между поисками plate и phone.
    // Если вызвали напрямую — применим на месте.
    $out = [];
    // Допустимые буквы для регистрационного знака: латиница + только те 12
    // кириллических, которые визуально совпадают с латиницей (А В Е К М Н О
    // Р С Т У Х). Слова «Машина», «Телефон», «Водитель» содержат буквы вне
    // этого набора (Ш, Л, Ф, И, Д, …) и сами целиком в кандидат не попадут.
    $A = 'A-Za-zАВЕКМНОРСТУХавекмнорстух';
    // Плотный фрагмент 4-12 символов, опционально хвост через одиночный «-»
    // или пробел. Перенос строки внутри не разрешён, чтобы не цепляться за
    // соседнюю строку (например «AM23245\nТелефон…»).
    // Word-boundary на старте: иначе жадный регекс склеит соседнюю «Ситроен»
    // с номером в один матч «троен АС6668» (получится отбракованная марка),
    // а сам номер «АС6668-5» уже не попробует.
    $patterns = [
        '/(?<![\p{L}\p{N}])[' . $A . '0-9]{4,12}(?:[\- ][' . $A . '0-9]{1,6})?/u',
        // Целевой Belarusian-plate-регекс: 1-2 буквы + 4-6 цифр + опц. «-N».
        // Нужен вторым проходом, иначе жадный матч выше съест соседний номер:
        // «Mercedes АВ1234-5» → жадно матчит «Mercedes АВ1234» (фильтруется
        // как 3+ букв), и «-5» уже не дотягивает до 4 чарактеров → теряем.
        '/(?<![\p{L}\p{N}])[' . $A . ']{1,2}\d{4,6}(?:-\d{1,2})?(?![\p{L}\p{N}])/u',
        // Аналогично для нового формата «1234AB-7» — цифры впереди.
        '/(?<![\p{L}\p{N}])\d{4,6}[' . $A . ']{1,2}(?:-\d{1,2})?(?![\p{L}\p{N}])/u',
    ];
    foreach ($patterns as $rx) {
        if (!preg_match_all($rx, $text, $m, PREG_OFFSET_CAPTURE)) continue;
        foreach ($m[0] as $cap) {
            [$frag, $pos] = $cap;
            $n = titNormalizePlate($frag);
            if (!$n['valid']) continue;
            // Кандидат должен иметь форму белорусского номера. Проверка по одной
            // длине (5–10 символов) пропускала мусор из подписи вроде «2A284D30»
            // и марки авто («ATEGO», «VOLVO»).
            if (!titLooksLikePlate($n['plate'])) continue;
            $out[] = ['plate' => $n['plate'], 'raw' => trim($frag), 'pos' => $pos];
        }
    }
    // Удаляем дубли (один номер найден разными регексами), порядок — по позиции.
    usort($out, fn($a, $b) => $a['pos'] <=> $b['pos']);
    $seen = [];
    $uniq = [];
    foreach ($out as $c) {
        if (isset($seen[$c['plate']])) continue;
        $seen[$c['plate']] = true;
        $uniq[] = $c;
    }
    return $uniq;
}

/**
 * Найти все кандидаты на телефон в тексте.
 */
function titFindPhoneCandidates(string $text): array
{
    $out = [];
    // Ищем последовательности с минимум 9 цифрами в окне. Разделители — только
    // пробел/таб, дефис, скобки, точка. Перенос строки внутри номера запрещён,
    // иначе жадный матч склеивает «+375297000001\n2)…» из соседней строки —
    // получается 13 цифр, кандидат отбраковывается, телефон теряется.
    if (!preg_match_all('/(\+?[ \t]*\d[\d \t\-\(\)\.]{8,20}\d)/u', $text, $m, PREG_OFFSET_CAPTURE)) {
        return $out;
    }
    foreach ($m[1] as $cap) {
        [$frag, $pos] = $cap;
        $n = titNormalizePhone($frag);
        if (!$n['valid']) continue;
        $out[] = ['phone' => $n['phone'], 'raw' => trim($frag), 'pos' => $pos];
    }
    // Уникализируем по нормализованному номеру (один и тот же телефон
    // в подписи и теле — это один кандидат).
    $seen = [];
    $uniq = [];
    foreach ($out as $c) {
        if (isset($seen[$c['phone']])) continue;
        $seen[$c['phone']] = true;
        $uniq[] = $c;
    }
    return $uniq;
}

/**
 * Основная функция: извлекает пары {plate, phone} из тела письма.
 *
 * Стратегия:
 *   - Чистим текст: убираем quote и подпись.
 *   - Собираем кандидатов на номера машин и телефоны.
 *   - Если ровно один номер и ровно один телефон — это одна пара.
 *   - Если несколько — связываем по минимальной дистанции в тексте.
 *   - Возвращаем массив пар. Пустой массив = ничего не распознано.
 *
 * @return array<int, array{plate: string, phone: string, plate_raw: string, phone_raw: string}>
 */
function titParseReplyBody(string $rawBody): array
{
    // Проход 1 — только «своя» часть письма (без цитат и подписей).
    $pairs = titParseCleanedText(titCleanReplyBody($rawBody), true);
    if ($pairs) return $pairs;

    // Проход 2 — данные лежат в пересланном письме. Закупщик часто просто
    // пересылает ответ поставщика («Пожалуйста, посодействуйте в приёмке»),
    // а номер машины и телефон водителя — внутри цитаты. Первый проход её
    // отрезает, поэтому здесь разбираем и цитату тоже: выбрасываем только
    // наше собственное исходящее письмо, шапки писем и строки подписей.
    //
    // Одинокий телефон здесь не принимаем: в пересланном письме подписей
    // несколько (наша и поставщика), и телефон без номера машины почти всегда
    // оказывается контактом менеджера, а не водителя.
    return titParseCleanedText(titCleanForwardedBody($rawBody), false);
}

/**
 * Готовит текст пересылки: цитату поставщика оставляем, но убираем шум,
 * цитату нашего исходящего письма и служебные шапки.
 */
function titCleanForwardedBody(string $rawBody): string
{
    $text = preg_replace("/\r\n|\r/", "\n", $rawBody) ?? $rawBody;

    $noise = [
        '/\[cid:[^\]]*\]/i',
        '/\bcid:[^\s>\]]+/i',
        '/<mailto:[^>]*>/i',
        '/<https?:\/\/[^>]*>/i',
        '/\bhttps?:\/\/\S+/i',
        '/\b[\w\.\-]+\.(?:png|jpe?g|gif|bmp|webp)\b/i',
        '/\[[^\]]*AI-generated content[^\]]*\]/i',
    ];
    foreach ($noise as $rx) {
        $text = preg_replace($rx, ' ', $text) ?? $text;
    }

    // Всё, что ниже цитаты НАШЕГО письма, — это текст заказа, данных о машине
    // там нет и быть не может. Режем, чтобы не искать номера в списке товаров.
    //
    // Маркером служит текст самого письма-заказа. По фразе «Отдел закупок»
    // резать нельзя: в пересылке она стоит в подписи сотрудника ВЫШЕ цитаты
    // с данными — так терялось всё письмо целиком.
    $ourMarkers = ['/(^|\n)[^\n]*Просьба\s+отгрузить\s+товар.*/su'];
    foreach ($ourMarkers as $rx) {
        if (preg_match($rx, $text, $m, PREG_OFFSET_CAPTURE)) {
            $text = substr($text, 0, $m[0][1]);
        }
    }

    // Построчно выбрасываем шапки писем и подписи. Именно построчно, а не
    // «обрезать хвост»: нужные данные могут идти ПОСЛЕ строки с адресом
    // отправителя цитаты («… <a.ivanova@postavshik.by> писал(а):»).
    $headerRx = [
        '/^\s*(?:From|To|Cc|Bcc|Sent|Subject|Date|Reply-To)\s*:/i',
        '/^\s*(?:От|Кому|Копия|Отправлено|Тема|Дата)\s*:/u',
    ];

    // Оставляем ровно одно письмо — ПЕРВОЕ пересланное:
    //  - то, что написал сам пересылающий, отбрасываем: его часть уже разобрал
    //    первый проход, а его подпись подсовывает телефон сотрудника вместо
    //    телефона водителя;
    //  - всё ниже второй цитаты тоже отбрасываем: там переписка прошлых
    //    поставок со своими номерами машин.
    $text = titExtractFirstQuotedLetter($text, $headerRx);
    if ($text === '') return '';

    $keep = [];
    foreach (explode("\n", $text) as $line) {
        $skip = titLineLooksLikeSignature($line);
        if (!$skip) {
            foreach ($headerRx as $rx) {
                if (preg_match($rx, $line)) { $skip = true; break; }
            }
        }
        if (!$skip) $keep[] = $line;
    }

    return trim(implode("\n", $keep));
}

/**
 * Возвращает текст ПЕРВОГО цитируемого письма: без части, которую написал
 * пересылающий, и без более старых цитат.
 *
 * Шапка одного письма («-----Original Message-----», затем From/Sent/To/
 * Subject подряд) считается одним блоком — данные идут сразу под ней. Вторая
 * шапка означает начало переписки прошлых дней.
 *
 * Пустая строка на выходе = цитаты в письме нет, разбирать в этом режиме нечего.
 *
 * @param string[] $headerRx регексы строк-заголовков письма
 */
function titExtractFirstQuotedLetter(string $text, array $headerRx): string
{
    $quoteRx = [
        '/-{2,}\s*Original Message\s*-{2,}/i',
        '/\bписал[аи]?\s*\(?а?\)?\s*:/u',
        '/\bwrote:\s*$/i',
        '/^\s*\d{2}\.\d{2}\.\d{4},\s*\d{1,2}:\d{2},\s*"/u',
    ];
    $isBoundary = function (string $line) use ($headerRx, $quoteRx): bool {
        foreach ($headerRx as $rx) if (preg_match($rx, $line)) return true;
        foreach ($quoteRx as $rx) if (preg_match($rx, $line)) return true;
        return false;
    };

    $lines = explode("\n", $text);
    $start = null;   // первая строка тела первой цитаты
    $inBlock = false;
    $seenFirst = false;

    foreach ($lines as $i => $line) {
        $boundary = $isBoundary($line);
        if ($boundary) {
            if (!$inBlock) {
                if ($seenFirst) {
                    // Вторая шапка — дальше старая переписка.
                    return $start === null ? '' : trim(implode("\n", array_slice($lines, $start, $i - $start)));
                }
                $seenFirst = true;
                $inBlock = true;
            }
            $start = $i + 1;   // тело цитаты начинается сразу за шапкой
        } elseif (trim($line) !== '') {
            if ($inBlock) $inBlock = false;   // содержательная строка закрывает шапку
        }
    }

    if (!$seenFirst || $start === null) return '';
    return trim(implode("\n", array_slice($lines, $start)));
}

/**
 * Общий разбор уже подготовленного текста. Раньше это было телом
 * titParseReplyBody — вынесено, чтобы прогнать два прохода (своя часть письма,
 * затем пересланная) одной и той же логикой.
 */
function titParseCleanedText(string $clean, bool $allowPhoneOnly = true): array
{
    if ($clean === '') return [];

    // Склеиваем разорванные пробелами номера ОДИН раз — чтобы позиции
    // plate и phone отсчитывались от одного и того же текста и жадное
    // связывание по дистанции работало правильно.
    $clean = titNormalizePlateSpacing($clean);

    $plates = titFindPlateCandidates($clean);
    $phones = titFindPhoneCandidates($clean);

    // Уникализуем номера машин: один и тот же номер в нескольких местах —
    // это один и тот же.
    $seen = [];
    $uniqPlates = [];
    foreach ($plates as $p) {
        if (isset($seen[$p['plate']])) continue;
        $seen[$p['plate']] = true;
        $uniqPlates[] = $p;
    }
    $plates = $uniqPlates;

    // Номера машин — основа. Но если номера нет, а телефон есть и письмо явно
    // про машину/водителя («Данные по машине и водителю. +375293232842,
    // Дмитрий») — раньше терялось всё, теперь отдаём телефон с пустым номером.
    // Закупщик увидит его в карточке письма и впишет номер руками.
    if (!$plates) {
        if (!$allowPhoneOnly || !$phones) return [];
        if (!preg_match('/\b(машин|водител|авто|транспорт|ттн|номер)/iu', $clean)) return [];
        return [[
            'plate' => '',
            'phone' => $phones[0]['phone'],
            'plate_raw' => '',
            'phone_raw' => $phones[0]['raw'],
        ]];
    }

    // Случай 1: один номер — независимо от количества телефонов привязываем
    // к нему первый найденный телефон (если он есть).
    if (count($plates) === 1) {
        $ph = $phones[0] ?? null;
        return [[
            'plate' => $plates[0]['plate'],
            'phone' => $ph['phone'] ?? '',
            'plate_raw' => $plates[0]['raw'],
            'phone_raw' => $ph['raw'] ?? '',
        ]];
    }

    // Случай 2: несколько номеров — связываем жадно по минимальной дистанции.
    // Сортируем пары (plate, phone) по дистанции между позициями, забираем
    // самые близкие первыми, без повторного использования. Если телефонов
    // меньше — оставшиеся номера сохраняем без телефона.
    $combos = [];
    foreach ($plates as $pi => $pl) {
        foreach ($phones as $hi => $ph) {
            $combos[] = ['pi' => $pi, 'hi' => $hi, 'dist' => abs($pl['pos'] - $ph['pos'])];
        }
    }
    usort($combos, fn($a, $b) => $a['dist'] <=> $b['dist']);
    $usedP = [];
    $usedH = [];
    $pairs = [];
    foreach ($combos as $c) {
        if (isset($usedP[$c['pi']]) || isset($usedH[$c['hi']])) continue;
        $usedP[$c['pi']] = true;
        $usedH[$c['hi']] = true;
        $pl = $plates[$c['pi']];
        $ph = $phones[$c['hi']];
        $pairs[] = [
            'plate' => $pl['plate'],
            'phone' => $ph['phone'],
            'plate_raw' => $pl['raw'],
            'phone_raw' => $ph['raw'],
        ];
    }
    // Добавляем оставшиеся номера без пары — телефонов на всех не хватило.
    foreach ($plates as $pi => $pl) {
        if (isset($usedP[$pi])) continue;
        $pairs[] = [
            'plate' => $pl['plate'],
            'phone' => '',
            'plate_raw' => $pl['raw'],
            'phone_raw' => '',
        ];
    }
    return $pairs;
}
