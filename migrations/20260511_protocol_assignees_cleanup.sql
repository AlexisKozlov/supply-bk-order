-- Чистка дублирующихся «соисполнителей» у протокольных карточек.
--
-- Источник дублей: для решения протокола с несколькими ответственными
-- создаётся отдельная карточка на доске каждого из них, но дополнительно
-- в tasks_assignees каждой карточки записываются ВСЕ остальные ответственные.
-- Из-за этого запрос «внешние карточки, где я соисполнитель» (в api/includes/tasks.php)
-- подтягивает копии с досок коллег на мою доску — одна задача показывается N раз.
--
-- Лечение в коде: rpc.php (pdSyncDecisionToCard) и
-- migrations/20260506_protocol_per_user_cards.php больше не пишут в
-- tasks_assignees для протокольных карточек. Эта миграция доочищает
-- то, что накопилось.

DELETE ta FROM tasks_assignees ta
INNER JOIN protocol_decision_cards pdc ON pdc.card_id = ta.card_id;
