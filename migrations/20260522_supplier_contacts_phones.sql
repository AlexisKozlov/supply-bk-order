-- restaurant_supplier_contacts: до 5 телефонов с подписями + имя необязательное
-- Раньше в карточке был один телефон. Теперь — до 5 номеров, у каждого своя
-- подпись (личный, рабочий, директор и т.п.) — храним в JSON. Существующая
-- колонка `phone` остаётся как «первый/основной» для обратной совместимости
-- (используется в группировке find-by-supplier и при экспортах).
--
-- phones_json — массив объектов: [{"phone": "+375...", "label": "Личный"}, ...].
-- На чтение бэк собирает массив телефонов; если колонка пуста, fallback —
-- одноэлементный массив из старого `phone`. Старые записи не трогаем.
--
-- name теперь NULL — раньше было NOT NULL. В UI карточки без имени
-- показываются как «(без имени)».

ALTER TABLE restaurant_supplier_contacts
  MODIFY COLUMN name VARCHAR(100) NULL,
  ADD COLUMN phones_json JSON NULL COMMENT '[{phone,label},...] — до 5 телефонов';
