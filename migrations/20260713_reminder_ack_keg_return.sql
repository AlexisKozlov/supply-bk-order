-- Возврат кег: кнопка «Сделал заявку» в TG-напоминании.
-- Механизм подтверждений (reminder_acknowledgements) уже работал для напоминаний
-- поставщикам и основной поставке; теперь тот же вид отметки нужен для кег —
-- иначе напоминание шлётся повторно, даже когда заявка уже подана вне портала.
--
-- keg_invoice («передайте накладные») добавляем сразу, чтобы потом не трогать
-- схему второй раз: напоминание одно, но отметка ему тоже пригодится.
ALTER TABLE reminder_acknowledgements
  MODIFY COLUMN reminder_kind ENUM('supplier','main_delivery','keg_return','keg_invoice') NOT NULL;
