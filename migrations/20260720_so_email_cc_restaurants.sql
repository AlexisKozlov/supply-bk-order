-- Копия сводки заявок ресторанам.
--
-- Когда сводка за день уходит поставщику письмом, рестораны, реально подавшие
-- заявку на эту дату, могут получить видимую копию. Настройка на поставщика,
-- по умолчанию выключена — поведение существующих поставщиков не меняется.

ALTER TABLE `so_supplier_settings`
  ADD COLUMN `email_cc_restaurants` TINYINT(1) NOT NULL DEFAULT 0
  COMMENT 'Ставить рестораны с заявками в копию письма поставщику'
  AFTER `auto_email_summary`;
