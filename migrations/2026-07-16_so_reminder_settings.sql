-- Настройки напоминаний по поставщику для модуля «Заявки поставщикам».
--
-- reminder_offsets  — какие тайминги напоминаний слать (CSV из белого списка
--                     evening,3h,2h,1h,30m,expired, в этом порядке).
-- reminder_channels — по каким каналам слать (CSV из белого списка tg,push).
--
-- Семантика значений:
--   NULL          = настройка не задавалась → дефолт (все тайминги / только Telegram).
--   ''            = пользователь ОСОЗНАННО отключил всё (напоминания не шлются),
--                   это НЕ подменяется дефолтом.
--   'a,b,c'       = явно выбранный список.
--
-- Обе колонки NULL по умолчанию, чтобы существующие строки поставщиков
-- продолжали работать на дефолтах без миграции данных.

ALTER TABLE so_supplier_settings
    ADD COLUMN reminder_offsets  VARCHAR(120) NULL DEFAULT NULL,
    ADD COLUMN reminder_channels VARCHAR(40)  NULL DEFAULT NULL;
