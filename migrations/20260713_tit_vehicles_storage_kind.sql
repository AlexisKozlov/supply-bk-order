-- Заявки на пропуск: тип хранения машины — три варианта вместо двух.
-- Раньше склад был только 1 (Прилесье 1, холод+мороз вместе) или 6 (Прилесье 6, сухой).
-- Теперь закупщик выбирает: сухой (Прилесье 6), холод (Прилесье 1), мороз (Прилесье 1).
--
-- Для охраны в xlsx НИЧЕГО не меняется: холод и мороз по-прежнему уходят как
-- склад 1 / код доступа 32, сухой — склад 6 / код 8. Колонка warehouse остаётся
-- источником этих чисел, storage_kind нужен только внутри портала.
--
-- Старые записи остаются с storage_kind = NULL: у них склад 1 означает
-- «холод/мороз» без уточнения, и додумывать за них мы не будем.
ALTER TABLE tit_vehicles
  ADD COLUMN storage_kind ENUM('DRY','COLD','FROZEN') NULL AFTER warehouse;
