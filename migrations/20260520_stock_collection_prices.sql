-- Цена за единицу товара в сборе остатков.
--
-- Зачем: админу нужно видеть в Excel и итогах не только остаток в штуках/кг/л,
-- но и сумму в деньгах. Цена привязана к конкретному сбору, а не к общему
-- каталогу: цены между сборами меняются, и в отчёте по сбору должна быть та
-- цена, которая была актуальна в момент сбора.
--
-- NULL = цена не задана (в расчёт не идёт). DECIMAL(12,4) с запасом по точности
-- для дробных цен на килограмм/литр.

ALTER TABLE stock_collection_products
  ADD COLUMN price DECIMAL(12, 4) NULL
    COMMENT 'Цена за единицу (Br); NULL — не задана, в стоимость не входит'
    AFTER unit;
