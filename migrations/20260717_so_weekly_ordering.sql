-- Недельный режим подачи заявок для модуля «Заявки поставщикам».
--
-- У поставщика может быть ОДНА общая отсечка на всю неделю доставки (пн–вс),
-- заданная как день недели + время на ПРЕДЫДУЩЕЙ неделе.
--
-- weekly_deadline_dow  — день недели дедлайна (1 = понедельник … 7 = воскресенье)
--                        на неделе, предшествующей неделе доставки.
--                        NULL = недельный режим ВЫКЛЮЧЕН (работают обычные правила).
-- weekly_deadline_time — время дедлайна (TIME). NULL при заданном dow → фолбэк
--                        default_deadline_time, а при его отсутствии → '14:00:00'.
--
-- Обе колонки NULL по умолчанию, чтобы существующие строки поставщиков
-- продолжали работать по-прежнему без миграции данных.

ALTER TABLE so_supplier_settings
    ADD COLUMN weekly_deadline_dow  TINYINT NULL DEFAULT NULL,
    ADD COLUMN weekly_deadline_time TIME    NULL DEFAULT NULL;
