-- Расширение reminder_runs.run_hour: TINYINT (0..255) → SMALLINT.
--
-- Старое значение хранило только ЧАС отправки (0..23 + 99 — финал-сообщение).
-- Из-за этого два слота reminder_times в один час (например 08:00 и 08:30)
-- дедуплицировались как один и второе сообщение не уходило.
--
-- Теперь храним HH*60+MM (0..1439), плюс «технические» значения:
--   1500 — pre-final (за 5 мин до дедлайна, было FINAL_RUN_HOUR_PRE = 98)
--   1501 — expired (после дедлайна, было FINAL_RUN_HOUR_EXPIRED = 99)
--
-- Старые записи в reminder_runs остаются совместимыми по контракту UNIQUE,
-- но «расходятся» с новой логикой (старое 99 = час 99, новое 1501 = expired).
-- Это OK: дедупликация считает день+слот; если в один день оба формата
-- встретятся — UNIQUE-индекс сработает корректно.

ALTER TABLE reminder_runs
  MODIFY COLUMN run_hour SMALLINT UNSIGNED NOT NULL COMMENT 'минутный слот HH*60+MM, или 1500/1501 для финальных';
