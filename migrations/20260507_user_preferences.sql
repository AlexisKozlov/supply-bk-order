-- Пользовательские настройки UI (избранное в сайдбаре и т. п.) хранятся
-- в JSON-колонке preferences, чтобы синхронизироваться между устройствами
-- и не сбрасываться при чистке кеша браузера. Раньше использовался только
-- localStorage в каждом браузере отдельно.
ALTER TABLE users
  ADD COLUMN preferences LONGTEXT NULL
  COMMENT 'Пользовательские UI-настройки (JSON): sidebar_pins и т.п.';
