-- Опции Excel-отчёта у поставщика для модуля «Заявки поставщикам».
--
-- xlsx_drop_empty     — 1 = убирать из отчёта пустые строки ресторанов
--                       (рестораны без заказанных позиций). 0 = показывать всех.
--                       По умолчанию 0 (прежнее поведение — показывать всех).
-- xlsx_pallet_metrics — какие показатели паллет/веса выводить в отчёте.
--                       CSV из значений белого списка: boxes,pallets,netto,brutto.
--                       NULL или пусто = паллеты/вес выключены (ничего не выводим).
--
-- Обе колонки с безопасными дефолтами, чтобы существующие строки поставщиков
-- работали без миграции данных: пустых строк не убираем, паллеты/вес выключены.

ALTER TABLE so_supplier_settings
    ADD COLUMN xlsx_drop_empty     TINYINT(1)  NULL DEFAULT 0,
    ADD COLUMN xlsx_pallet_metrics VARCHAR(60) NULL DEFAULT NULL;
