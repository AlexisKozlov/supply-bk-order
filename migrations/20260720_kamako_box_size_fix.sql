-- Камако: «в коробке» описывало ведро, а не коробку.
--
-- У майонеза «Провансаль» и соуса «Сырный» стояло qty_per_box = 1 и вес
-- 1000 г — то есть характеристики ведра 1 кг. Из-за этого в отчёте заявки
-- 180 кг превращались в 180 «коробок». Реальная коробка — 6 кг (совпадает с
-- кратностью заказа), подтверждено закупками 20.07.2026.
--
-- Вес брутто пересчитан в той же пропорции (вес ведра × 6).
-- boxes_per_pallet = 100 не трогаем: 100 коробок × 6 кг = 600 кг на паллете —
-- правдоподобно, а прежние 100 кг на паллету не были.

UPDATE `products`
SET `qty_per_box`   = 6,
    `weight_netto`  = 6000.00,
    `weight_brutto` = 6354.00
WHERE `sku` = '161124' AND `qty_per_box` = 1;

